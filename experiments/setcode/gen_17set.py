"""17-低圧セットコード 72件を新設計(属性+展開)でDB追加候補JSONに起こす。
 単価はコード表p9の古川値。展開はセットコード内容(p9下段)に基づく。
 確定部品=DB実コード / 未確定(VS/AS統合, CT変流比, LUG容量)=容量連動プレースホルダ(要確認)。"""
import json
db = json.load(open('db.json', encoding='utf-8'))
idx = {d['code']: d for d in db}

TR = {('1φ3W',30):'45002',('1φ3W',50):'45003',('1φ3W',75):'45004',('1φ3W',100):'45005',
      ('1φ3W',150):'45006',('1φ3W',200):'45007',('1φ3W',300):'45008',('1φ3W',500):'45009',
      ('3φ3W',30):'45032',('3φ3W',50):'45033',('3φ3W',75):'45034',('3φ3W',100):'45035',
      ('3φ3W',150):'45036',('3φ3W',200):'45037',('3φ3W',300):'45038',('3φ3W',500):'45039',('3φ3W',750):'45040',
      ('スコット',30):'45054',('スコット',50):'45056',('スコット',75):'45057',('スコット',100):'45058',
      ('スコット',150):'45059',('スコット',200):'45060',('スコット',300):'45061'}

METER = {
 '広角':[('42002','VM(広角)',1),('42012','AM(広角)',1)],
 '普通角':[('42001','VM(普通角)',1),('42010','AM(普通角)',1)],
 'マルチ':[('42082','マルチ指示計(ME110GR)',1)],
}

def expand(meter, phase, kva):
    parts = []
    parts.append(('71091','FL-10W(ドアSW付)',1))
    parts.append(('43321','LBS 3P200A(PF・AL)',1))
    tr = TR.get((phase,kva))
    parts.append((tr, 'TR(支給品) %s %sKVA' % (phase,kva), 1))
    parts.append(('46401','LG-RY(ZCT付)',1))
    scott = (phase=='スコット')
    mult = 2 if scott else 1
    if meter=='マルチ':
        parts.append(('42082','マルチ指示計',mult))   # スコットは×2
    else:
        for code,nm,q in METER[meter]:
            parts.append((code,nm,q*mult))
        parts.append(('71001','VS',1*mult))
        parts.append(('71002','AS',1*mult))
    parts.append(('CT?','CT 変流比=TR二次電流連動(要サイジング確認)', 4 if scott else 2))
    parts.append(('LUG?','M)LUG 3P 容量=TR二次電流連動(要サイジング確認)', mult))
    return parts

DATA = {
 ('広角','1φ3W'):{30:('17002',351),50:('17003',371),75:('17004',395),100:('17005',423),150:('17006',445),200:('17007',516),300:('17008',614),500:('17009',908)},
 ('広角','3φ3W'):{30:('17032',357),50:('17033',377),75:('17034',411),100:('17035',439),150:('17036',457),200:('17037',521),300:('17038',620),500:('17039',796),750:('17040',1120)},
 ('広角','スコット'):{30:('17052',447),50:('17053',469),75:('17054',509),100:('17055',537),150:('17056',557),200:('17057',625),300:('17058',713)},
 ('普通角','1φ3W'):{30:('17102',339),50:('17103',359),75:('17104',383),100:('17105',411),150:('17106',433),200:('17107',504),300:('17108',602),500:('17109',896)},
 ('普通角','3φ3W'):{30:('17132',345),50:('17133',365),75:('17134',399),100:('17135',427),150:('17136',445),200:('17137',509),300:('17138',608),500:('17139',784),750:('17140',1108)},
 ('普通角','スコット'):{30:('17152',423),50:('17153',445),75:('17154',485),100:('17155',513),150:('17156',533),200:('17157',601),300:('17158',689)},
 ('マルチ','1φ3W'):{30:('17202',387),50:('17203',407),75:('17204',431),100:('17205',459),150:('17206',481),200:('17207',552),300:('17208',650),500:('17209',944)},
 ('マルチ','3φ3W'):{30:('17232',393),50:('17233',413),75:('17234',447),100:('17235',475),150:('17236',493),200:('17237',557),300:('17238',656),500:('17239',832),750:('17240',1156)},
 ('マルチ','スコット'):{30:('17252',519),50:('17253',541),75:('17254',581),100:('17255',609),150:('17256',629),200:('17257',697),300:('17258',785)},
}

out = []
for (meter,phase),caps in DATA.items():
    for kva,(code,fk) in caps.items():
        exp = expand(meter,phase,kva)
        tr_ok = TR.get((phase,kva)) is not None
        out.append({
            'code':code,
            'name':'低圧セット %s %s %sKVA' % (meter,phase,kva),
            'kind':'配電盤','cat':'低圧セット',
            'settype':'低圧','meter':meter,'phase':phase,'cap':'%sKVA'%kva,
            'ax':'','volt':'低圧','shikyu':'','sp':'','af':'','spec':'%s/%s/%sKVA'%(meter,phase,kva),
            'furukawa':str(fk),
            'expand':[{'code':c,'name':n,'qty':q} for c,n,q in exp],
            '_tr_resolved':tr_ok,
        })

json.dump(out, open('cand_17set.json','w',encoding='utf-8'), ensure_ascii=False, indent=1)
print('生成 %d件 -> cand_17set.json' % len(out))
miss_tr = [o['code'] for o in out if not o['_tr_resolved']]
dup = [o['code'] for o in out if o['code'] in idx]
print('TR未解決:', miss_tr or 'なし')
print('既存DBと衝突:', dup or 'なし')
for o in out:
    if o['code']=='17206':
        print('\n--- 見本 17206 (マルチ 1φ3W 150KVA, 古川481) 展開 ---')
        for p in o['expand']:
            print('    %-7s x%-3s %s' % (p['code'], p['qty'], p['name']))
